
package com.practice.java;

public class DataTypes {
    
    String str_var ="Product";
    int num=5;
    float myFloat=50.10f;
    char myChar='h';
    boolean myBoolean=true;
    byte myByte=12;
    double myDouble=15.00001;
    long myLong;
    short myShort;

    

    public static void main(String[] args) {

        DataTypes obj= new DataTypes();
        
        System.out.println(obj.str_var);
        System.out.println(obj.num);
        System.out.println(obj.myFloat);
        System.out.println(obj.myLong);
        System.out.println(obj.myDouble);
        System.out.println(obj.myChar);
        System.out.println(obj.myBoolean);
        System.out.println(obj.myByte);
        System.out.println(obj.myShort);
        

    }

}
